    private void login() {
        String name = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        User user = MainService.login(name, password);
        LoggedUserService.getInstance().setUser(user);

        if (user != null) {
            new MainWindow();
            dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Name or password is wrong!", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }

    private void register() {
        String name = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        User user = new User(name, password);

        if (MainService.register(user)) {
            JOptionPane.showMessageDialog(null, "Registered successfully!");

        } else {

            JOptionPane.showMessageDialog(null, "Faild!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

